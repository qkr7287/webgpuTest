const fs = require('fs')
const path = require('path')
const p = path.join(__dirname, '../public/env/free_cyberpunk_hovercar.glb')
const buf = fs.readFileSync(p)
const jsonLen = buf.readUInt32LE(12)
const json = JSON.parse(buf.slice(20, 20 + jsonLen).toString('utf8'))
console.log('animations:', json.animations ? json.animations.length : 0)
if (json.animations?.length) console.log('names:', json.animations.map(a => a.name))
console.log('skins:', json.skins ? json.skins.length : 0)
